
public class Bitwiseop {
    
    public static void main(String[] args) {
        System.out.println(5&6);
        System.out.println(5|6);
        System.out.println(5^6);
        System.out.println(~5); //one's complement
        System.out.println(~0);

        // a<<b = a * 2^b
        System.out.println(5<<2); // left shift 
        
         // a>>b = a / 2^b
         System.out.println(5>>2); // left shift 
        

        


    }
}
